package Utilities;


public class Conta {

    private int numero;
    private String titular, tipo;
    protected double saldo, valor;

    public Conta(int numero, String titular, String tipo, double saldo, double valor) {
        this.numero = numero;
        this.titular = titular;
        this.tipo = tipo;
        this.saldo = saldo;
        this.valor = valor;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getTitular() {
        return titular;
    }

    public void setTitular(String titular) {
        this.titular = titular;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }
}
